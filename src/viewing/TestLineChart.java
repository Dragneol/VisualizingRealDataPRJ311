    private void drawBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_drawBtnActionPerformed
        // TODO add your handling code here:
        //get setup value
        double minY = Double.parseDouble(minValuteTxt.getText());
        double maxY = Double.parseDouble(maxValueTxt.getText());
        String xLabel = xLabelTxt.getText();
        String yLabel = yLabelTxt.getText();
        if (rList != null) {
            rList.minX = minY;
            rList.maxY = maxY;
            rList.minX = 0;
            rList.maxX = rList.size();
            pChart.setxLabel(xLabel);
            pChart.setyLabel(yLabel);
            pChart.drawChart(rList);
            this.repaint();
        }
    }//GEN-LAST:event_drawBtnActionPerformed

    private void getExtremeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getExtremeBtnActionPerformed
        // TODO add your handling code here:
        String contents = this.dataTxt.getText();
        String[] valueStrs = contents.split("\n");
        double[] values = new double[valueStrs.length];
        for (int i = 0; i < valueStrs.length; i++) {
            values[i] = Double.parseDouble(valueStrs[i]);
        }
        rList = new RealPointList(values);
        rList.getBoundaries();
        extremeMaxValueTxt.setText(rList.maxY + "");
        extremeMinValueTxt.setText(rList.minY + "");
        drawBtn.setEnabled(true);
    }//GEN-LAST:event_getExtremeBtnActionPerformed

